

# 常用工具
class Util:

  # Trim
  def trim(str: str):
    return str.strip()
  
  # Ltrim
  def ltrim(str: str):
    return str.lstrip()
  
  # Rtrim
  def rtrim(str: str):
    return str.rstrip()
  
  # Lower
  def lower(str: str):
    return str.lower()
  
  # Upper
  def upper(str: str):
    return str.upper()
 