

# 常用工具
class Util:

  # Trim
  def Trim(str: str):
    return str.strip()
  
  # Ltrim
  def Ltrim(str: str):
    return str.lstrip()
  
  # Rtrim
  def Rtrim(str: str):
    return str.rstrip()
  
  # Lower
  def Lower(str: str):
    return str.lower()
  
  # Upper
  def Upper(str: str):
    return str.upper()
 